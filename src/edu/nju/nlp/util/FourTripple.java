package edu.nju.nlp.util;

public class FourTripple {

	/**
	 * @param args
	 */
	public double v1;
	public double v2;
	public double v3;
	public double v4;
	
	public FourTripple(double v1, double v2, double v3, double v4) {
		super();
		this.v1 = v1;
		this.v2 = v2;
		this.v3 = v3;
		this.v4 = v4;
	}

}
